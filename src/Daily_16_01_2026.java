    public static int maximizeSquareArea(int m, int n, int[] hFences, int[] vFences) {
        int[] hf = Arrays.copyOf(hFences, hFences.length + 2);
        int[] vf = Arrays.copyOf(vFences, vFences.length + 2);

        // add the boundries
        hf[hFences.length] = 1;
        hf[hFences.length + 1] = m;
        // System.out.println("hf " + Arrays.toString(hf));

        vf[vFences.length] = 1;
        vf[vFences.length + 1] = n;

        HashSet<Integer> vset = new HashSet<>();
        for (int i : vf) {
            vset.add(i);
        }

        // 600 * 600 * 600
        // 216 * 10 ^ 6
        // 10^8
        int maxArea = -1;
        for (int i = 0; i < hf.length; i++) {
            for (int j = i; j < hf.length; j++) {
                int h1 = hf[i], h2 = hf[j];
                for (int k = 0; k < vf.length; k++) {
                    int v1 = vf[k];
                    int len = Math.abs(h1 - h2);
                    if (vset.contains(v1 - len) || vset.contains(v1 + len)) {
                        maxArea = Math.max(maxArea, len * len);
                        // System.out.println("h1= " + h1 + " h2= " + h2 + " v1= " + v1 + " len= " +
                        // len);
                    }
                }
            }
        }
        return maxArea;
    }

